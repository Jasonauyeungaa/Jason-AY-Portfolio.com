// Current language
let currentLang = localStorage.getItem('preferredLanguage') || 'en';

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLang);
    
    // Close dropdown when clicking outside
    document.addEventListener('click', (e) => {
        if (!e.target.closest('.lang-switcher')) {
            document.querySelectorAll('.lang-dropdown').forEach(d => d.classList.remove('show'));
        }
    });
});

// Toggle language dropdown
function toggleLangDropdown() {
    document.querySelectorAll('.lang-dropdown').forEach(d => d.classList.toggle('show'));
}

// Set language function
function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('preferredLanguage', lang);
    document.documentElement.lang = lang;
    
    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
    
    // Close dropdown
    document.querySelectorAll('.lang-dropdown').forEach(d => d.classList.remove('show'));
    
    // Apply translations
    applyTranslations();
    updatePageTitle();
}

// Update page title
function updatePageTitle() {
    const titles = {
        'en': 'Jason Au-Yeung | IEEM × Data × Tech',
        'zh-TW': 'Jason Au-Yeung | 工業工程 × 數據 × 科技',
        'zh-CN': 'Jason Au-Yeung | 工业工程 × 数据 × 科技',
        'es': 'Jason Au-Yeung | IEEM × Data × Tech'
    };
    if (titles[currentLang]) {
        document.title = titles[currentLang];
    }
}

// Apply translations to page
function applyTranslations() {
    const t = translations[currentLang];
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (t[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                el.textContent = t[key];
            }
        }
    });
    
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.dataset.i18nHtml;
        if (t[key]) {
            el.innerHTML = t[key];
        }
    });
}


// Counter animation for stats
function animateCounter(element, target) {
    const duration = 2000;
    const start = 0;
    const increment = target / (duration / 16);
    let current = start;
    
    const timer = setInterval(() => {
        current += increment;
        if (current >= target) {
            element.textContent = target + (target === 130 ? '+' : '');
            clearInterval(timer);
        } else {
            element.textContent = Math.floor(current) + (target === 130 ? '+' : '');
        }
    }, 16);
}

// Initialize counter animation on page load
document.addEventListener('DOMContentLoaded', () => {
    const statNumbers = document.querySelectorAll('.stat-number');
    statNumbers.forEach((stat, index) => {
        const text = stat.textContent.trim();
        if (text === '130+') {
            animateCounter(stat, 130);
        } else if (text === '14') {
            animateCounter(stat, 14);
        } else if (text === '4') {
            animateCounter(stat, 4);
        }
    });
});
